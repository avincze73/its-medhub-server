package jj2000.j2k.encoder;

public class J2KTDSException extends Exception
{
	public J2KTDSException()
	{
		super();
	}

	public J2KTDSException(String message)
	{
		super(message);
	}

	public J2KTDSException(String message, Throwable th)
	{
		super(message, th);
	}

	public J2KTDSException(Throwable th)
	{
		super(th);
	}
}
